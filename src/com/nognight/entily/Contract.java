package com.nognight.entily;

public class Contract {
	private int contract_id;
	private String contract_name;
	private String contract_content;
	private String contract_remark;

	public int getContract_id() {
		return contract_id;
	}
	public void setContract_id(int contract_id) {
		this.contract_id = contract_id;
	}
	public String getContract_name() {
		return contract_name;
	}
	public void setContract_name(String contract_name) {
		this.contract_name = contract_name;
	}
	public String getContract_content() {
		return contract_content;
	}
	public void setContract_content(String contract_content) {
		this.contract_content = contract_content;
	}
	public String getContract_remark() {
		return contract_remark;
	}
	public void setContract_remark(String contract_remark) {
		this.contract_remark = contract_remark;
	}

	
}
