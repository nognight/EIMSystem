package com.nognight.entily;

public class Manager {
	private int manager_id;
	private String manager_username;
	private String manager_password;
	private String manager_position;
	private String manager_info;
	public int getManager_id() {
		return manager_id;
	}
	public void setManager_id(int manager_id) {
		this.manager_id = manager_id;
	}
	public String getManager_username() {
		return manager_username;
	}
	public void setManager_username(String manager_username) {
		this.manager_username = manager_username;
	}
	public String getManager_password() {
		return manager_password;
	}
	public void setManager_password(String manager_password) {
		this.manager_password = manager_password;
	}
	public String getManager_position() {
		return manager_position;
	}
	public void setManager_position(String manager_position) {
		this.manager_position = manager_position;
	}
	public String getManager_info() {
		return manager_info;
	}
	public void setManager_info(String manager_info) {
		this.manager_info = manager_info;
	}

}
